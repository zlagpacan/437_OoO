# daxpy loop
daxpy_loop:

    # for (i = 0; i != 0x800; ...
        # i = $t0 is really 4 x i
    ori $a1, $zero, 2
    srlv $a1, $a1, $t0
    beq $a1, $s3, after_loop

    # load A
    lw $t1, 0($s0)

    # load X[i] = *(X + i)
    add $a0, $s1, $t0
    lw $t2, 0($a0) 

    # load Y[i] = *(Y + i)
    add $a0, $s2, $t0
    lw $t3, 0($a0)

    # A + X + Y
    add $t4, $t1, $t2
    add $t4, $t4, $t3

    # store A + X + Y @ Z[i]
    add $a0, $s4, $t0
    sw $t4, 0($a0)

    # ... i++)
        # really $t0 + 4
    addi $t0, $t0, 4
    j daxpy_loop

after_loop:
